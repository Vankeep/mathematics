public class PlaneIntersection {
    public static void main(String[] args) {
        // Координаты начальной и конечной точек прямой
        float[] p1 = new float[]{0,0,0};
        float[] p2 = new float[]{10,15,12};
        // Уравнение плоскости x - 255 = 0 (гугл в помощь как его составить для необходимой плоскости)
        float planeX = 255;

        // Находим направляющие коэффициенты прямой
        float a = p2[0] - p1[0];
        float b = p2[1] - p1[1];
        float c = p2[2] - p1[2];

        // Находим параметр t, при котором прямая пересекает плоскость
        float t = (planeX - p1[0]) / a;

        // Находим координаты пересечения
        float x = p1[0] + a * t;
        float y = p1[1] + b * t;
        float z = p1[2] + c * t;

        // Выводим координаты пересечения
        System.out.println("Координаты пересечения: (" + x + ", " + y + ", " + z + ")");
    }
}
